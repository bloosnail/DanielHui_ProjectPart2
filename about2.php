							<h3>Background</h3>
							<p>I was born in Johns Hopkins Hospital (in Baltimore, Maryland) on June 19th, 1995, the third and last child of my parents. After a few years we moved to St. Louis, Missouri, and when I was in 4th grade we moved to my current hometown of Radnor, Pennsylvania, where I attended Radnor High School. I decided to attend the University of Pittsburgh and have been there since, and will be graduating this Spring. I am now looking for post-graduate opportunities out-of-state -- at the moment it looks like I will be moving to either Baltimore, Boston, or the Bay Area. I would like to get more experience (by that I mean take a break from classes) first and apply to PhD programs in a year or two with a very strong focus on what I would like to study.</p>
							
